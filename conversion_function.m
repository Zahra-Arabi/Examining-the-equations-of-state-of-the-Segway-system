% تعریف پارامترهای سیستم
M = 1;  % جرم بدنه
m = 0.1;  % جرم چرخ
l = 0.5;  % فاصله مرکز جرم بدنه تا محور چرخ
I = 0.006;  % ممان اینرسی بدنه حول مرکز جرم
g = 9.81;  % شتاب گرانش

% ماتریس‌های سیستم خطی شده
A = [0, 1, 0, 0;  
     0, 0, m*l/(M+m), 0;  
     0, 0, 0, 1; 
     0, 0, (M+m)*g/(I + m*l^2), 0];

B = [0; 
     1/(M+m); 
     0; 
     -m*l/((M+m)*(I + m*l^2))];

C = [1, 0, 0, 0; 
     0, 0, 1, 0];

D = [0; 0];

% تعریف متغیر سمبلیک s
syms s

% تعریف ماتریس همانی با ابعاد 4x4
I = eye(4);

% محاسبه SI - A
SI_A = s*I - A;

% محاسبه تابع تبدیل
sys_tf = C * inv(SI_A) * B;

disp(sys_tf);

